list_ = [1, 2, 3, 4, 5, 6]

# print whole list
print(list_[:])

# print list from 1st to 4th index
sublist_1_4 = list_[1: 5]
print(sublist_1_4)

# print list from Start to 3rd index
sublist = list_[0: 4]
print(sublist)

sublist = list_[: 4]
print(sublist)

# print list from 3rd to end
sublist = list_[3: 6]
print(sublist)

sublist = list_[3:]
print(sublist)
